// テキストの類似度を計算する関数
export const calculateSimilarity = (text1, text2) => {
    const words1 = text1.toLowerCase().split(/\s+/);
    const words2 = text2.toLowerCase().split(/\s+/);
    
    const set1 = new Set(words1);
    const set2 = new Set(words2);
    
    const intersection = new Set([...set1].filter(x => set2.has(x)));
    const union = new Set([...set1, ...set2]);
    
    return intersection.size / union.size;
};

// 問い合わせ内容に基づいて関連するFAQを検索する関数
export const findRelatedFAQs = (message, service) => {
    const relatedFAQs = [];
    
    FAQ_DATA.forEach(faq => {
        // サービスが一致するかチェック
        if (!faq.services.includes(service)) {
            return;
        }
        
        let score = 0;
        
        // キーワードマッチング
        faq.keywords.forEach(keyword => {
            if (message.toLowerCase().includes(keyword.toLowerCase())) {
                score += 0.3; // キーワードマッチで0.3点
            }
        });
        
        // 質問文との類似度
        const questionSimilarity = calculateSimilarity(message, faq.question);
        score += questionSimilarity * 0.7; // 類似度で0.7点
        
        if (score > 0.2) { // スコアが0.2を超える場合のみ関連として扱う
            relatedFAQs.push({
                ...faq,
                score: score
            });
        }
    });
    
    // スコアの高い順にソート（最大3件まで）
    return relatedFAQs
        .sort((a, b) => b.score - a.score)
        .slice(0, 3);
};

// HTMLエスケープ関数
export const escapeHtml = (str) => {
    if (typeof str !== 'string') {
        if (str === null || str === undefined) return '';
        return String(str);
    }
    return str
        .replace(/&/g, '&amp;')
        .replace(/</g, '&lt;')
        .replace(/>/g, '&gt;')
        .replace(/"/g, '&quot;')
        .replace(/'/g, '&#039;');
}; 